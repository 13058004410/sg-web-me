    '--tg-table-odd-background': color.value
      ? 'var(--tg-secondary-grey)'
      : 'initial',
  }
})

function changeHidden() {
  if (isHidden.value)
    setIsHiddenFalse()
  else
    setIsHiddenTrue()
}

watch(() => props.tabVal, (newValue) => {
  if (newValue)
    activeTab.value = newValue
})
watch(() => activeTab.value, (newValue) => {
  if (newValue === 'casino-mine')
    runCasinoRecordList({})
  if (['casino-all', 'casino-fy'].includes(activeTab.value)) {
    (!timer.value) && (timer.value = setInterval(() => {
      setColor(!color.value)
    }, 3000))
  }
  else {
    timer.value && clearInterval(timer.value)
    timer.value = null
    setColor(!props.showTab)
  }
}, { immediate: true })
watch(() => isLogin.value, (newValue) => {
  if (newValue) {
    runCasinoRecordList({}).then(() => {
      if (!list.value.length)
        emit('delMine')
    })
  }
}, { immediate: true })

onUnmounted(() => {
  timer.value && clearInterval(timer.value)
})
</script>

