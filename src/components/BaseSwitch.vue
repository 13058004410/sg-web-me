<script setup lang="ts">
interface Props {
  modelValue: boolean
}
const props = defineProps<Props>()
const emit = defineEmits(['update:modelValue', 'change'])

function onClick() {
  emit('update:modelValue', !props.modelValue)
  emit('change', !props.modelValue)
}
</script>

<template>
  <div class="base-switch" :class="{ active: modelValue }" @click="onClick">
    <div class="circle" />
  </div>
</template>

<style scoped lang="scss">
.base-switch {
  width: var(--tg-spacing-40);
  height: var(--tg-spacing-24);
  background-color: var(--tg-secondary-main);
  padding: var(--tg-spacing-2);
  border-radius: 100px;
  cursor: pointer;

  .circle {
    width: var(--tg-spacing-20);
    height: var(--tg-spacing-20);
    background-color: var(--tg-text-white);
    border-radius: 50%;
    transition: all ease-out .3s;
  }
}

.active {
  background-color: var(--tg-text-blue);

  .circle {
    transform: translateX(var(--tg-spacing-16));
  }
}
</style>
